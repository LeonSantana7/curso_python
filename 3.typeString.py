"""

String -> str -> texto
Strings são textos que estão dentro de aspas

"""

print("1234")

# Aspas simples
print(type('Leon Santana'))
print(1,'Leon "Santana"', sep="-")

# Aspas duplas
print("Leon Santana", end=" \n Next:")

# Escape
print("Leon \"Santana\"")

# r
print(r"Leon \"Santana\"")

# Repetição
print("ha" * 3)

# Métodos
print("leon".upper())